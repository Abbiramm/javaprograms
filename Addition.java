package Add;

public class Addition {

	
	int i,j,s;
	
	   public int add(int i,int j)
	{
		this.i=i;
		this.j=j;
		s=i+j;
		return s;

	}
	   public int sub(int i,int j)
		{
			this.i=i;
			this.j=j;
			s=i-j;
			return s;

		}
	   public int mul(int i,int j)
		{
			this.i=i;
			this.j=j;
			s=i*j;
			return s;

		}
	   public int div(int i,int j)
		{
			this.i=i;
			this.j=j;
			s=i/j;
			return s;

		}

}
